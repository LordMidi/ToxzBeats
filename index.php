/**
 * Format the beat name of display purposes.
 * @param $name
 * @return string
 */
function formatBeatName($name) {
    $name = str_replace('Toxz - ', '', $name);
    $name = preg_split('/(?=_)/', $name, -1, PREG_SPLIT_NO_EMPTY)[0];
    return $name;
}
